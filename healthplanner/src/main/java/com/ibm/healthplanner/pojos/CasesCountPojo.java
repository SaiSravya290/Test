package com.ibm.healthplanner.pojos;

public class CasesCountPojo {


	public String state;
	
	public String negative;
	
	public String positive;
	
	public String totaltested;
	
	public String unconfirmed;
	
	public String updatedon;
	
	public String testspermillion;
	
	
	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getNegative() {
		return negative;
	}

	public void setNegative(String negative) {
		this.negative = negative;
	}

	public String getPositive() {
		return positive;
	}

	public void setPositive(String positive) {
		this.positive = positive;
	}

	public String getTotaltested() {
		return totaltested;
	}

	public void setTotaltested(String totaltested) {
		this.totaltested = totaltested;
	}

	public String getUnconfirmed() {
		return unconfirmed;
	}

	public void setUnconfirmed(String unconfirmed) {
		this.unconfirmed = unconfirmed;
	}

	public String getUpdatedon() {
		return updatedon;
	}

	public void setUpdatedon(String updatedon) {
		this.updatedon = updatedon;
	}

	public String getTestspermillion() {
		return testspermillion;
	}

	public void setTestspermillion(String testspermillion) {
		this.testspermillion = testspermillion;
	}




}
